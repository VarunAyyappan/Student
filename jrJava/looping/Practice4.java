package jrJava.looping;

public class Practice4 {

	public static void main(String[] args) {
		
		
		//1+2+3+ ...+1000
		int sum = 0;
		for(int i=1; i<=1000; i=i+1){
			sum = sum + i;
		}
		System.out.println(sum);
		
		//1+3+5+7+...+99
		sum = 0;
		for(int i=1; i<=99; i=i+2){
			sum = sum + i;
		}
		System.out.println(sum);
		
		
		//213+212+211+210+ ... +(-56)+(-57)+(-58)
		
		//11+14+17+...+107
		
		//1*1 + 2*2 + 3*3 + ... + 100*100
		
		//1*2 + 2*3 + 3*4 + ... + 99*100
		
	}

}









